"use client";

import { useState } from "react";
import { useRouter } from "next/navigation";
import Link from "next/link";
import { useFormStatus } from "react-dom";
import { register, loginWithGoogle, loginWithDiscord } from "@/actions/auth.actions";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Checkbox } from "@/components/ui/checkbox";
import { Separator } from "@/components/ui/separator";
import { useToast } from "@/hooks/use-toast";
import { User, Mail, Lock } from "lucide-react";

export function RegisterForm() {
  const [error, setError] = useState<string | null>(null);
  const router = useRouter();
  const { toast } = useToast();

  async function handleSubmit(formData: FormData) {
    setError(null);

    const password = formData.get("password") as string;
    const confirmPassword = formData.get("confirmPassword") as string;

    if (password !== confirmPassword) {
      setError("Passwords do not match");
      return;
    }

    const result = await register(formData);

    if (result?.error) {
      setError(result.error);
      toast({
        title: "Registration failed",
        description: result.error,
        variant: "destructive",
      });
    } else {
      toast({
        title: "Account created",
        description: "Welcome to MudListings!",
      });
      router.push("/");
      router.refresh();
    }
  }

  return (
    <div className="space-y-6">
      <form action={handleSubmit} className="space-y-4">
        <div className="space-y-2">
          <Label htmlFor="displayName">Username</Label>
          <div className="relative">
            <User className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              id="displayName"
              name="displayName"
              placeholder="Choose a username"
              required
              minLength={2}
              maxLength={50}
              className="pl-10 bg-input border-white/10"
            />
          </div>
          <p className="text-xs text-muted-foreground">
            This will be your display name on MudListings
          </p>
        </div>

        <div className="space-y-2">
          <Label htmlFor="email">Email Address</Label>
          <div className="relative">
            <Mail className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              id="email"
              name="email"
              type="email"
              placeholder="you@example.com"
              required
              autoComplete="email"
              className="pl-10 bg-input border-white/10"
            />
          </div>
        </div>

        <div className="space-y-2">
          <Label htmlFor="password">Password</Label>
          <div className="relative">
            <Lock className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              id="password"
              name="password"
              type="password"
              placeholder="••••••••"
              required
              minLength={8}
              autoComplete="new-password"
              className="pl-10 bg-input border-white/10"
            />
          </div>
          <p className="text-xs text-muted-foreground">
            Must be at least 8 characters
          </p>
        </div>

        <div className="space-y-2">
          <Label htmlFor="confirmPassword">Confirm Password</Label>
          <div className="relative">
            <Lock className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
            <Input
              id="confirmPassword"
              name="confirmPassword"
              type="password"
              placeholder="••••••••"
              required
              minLength={8}
              autoComplete="new-password"
              className="pl-10 bg-input border-white/10"
            />
          </div>
        </div>

        <div className="space-y-3 pt-2">
          <div className="flex items-start space-x-2">
            <Checkbox id="terms" name="terms" className="mt-0.5" required />
            <Label
              htmlFor="terms"
              className="text-sm font-normal text-muted-foreground cursor-pointer leading-relaxed"
            >
              I agree to the{" "}
              <Link href="/terms" className="text-primary hover:text-primary/80">
                Terms of Service
              </Link>{" "}
              and{" "}
              <Link href="/privacy" className="text-primary hover:text-primary/80">
                Privacy Policy
              </Link>
            </Label>
          </div>
          <div className="flex items-start space-x-2">
            <Checkbox id="updates" name="updates" className="mt-0.5" />
            <Label
              htmlFor="updates"
              className="text-sm font-normal text-muted-foreground cursor-pointer leading-relaxed"
            >
              Send me updates about new MUDs and community news
            </Label>
          </div>
        </div>

        {error && <p className="text-sm text-destructive">{error}</p>}

        <SubmitButton>Create Account</SubmitButton>
      </form>

      <div className="relative">
        <div className="absolute inset-0 flex items-center">
          <Separator className="w-full" />
        </div>
        <div className="relative flex justify-center text-xs uppercase">
          <span className="bg-card px-2 text-muted-foreground">
            Or sign up with
          </span>
        </div>
      </div>

      <div className="grid grid-cols-2 gap-4">
        <form action={loginWithGoogle}>
          <Button
            variant="outline"
            type="submit"
            className="w-full bg-transparent border-white/10 hover:bg-white/5"
          >
            <GoogleIcon className="h-4 w-4 mr-2" />
            Google
          </Button>
        </form>
        <form action={loginWithDiscord}>
          <Button
            variant="outline"
            type="submit"
            className="w-full bg-transparent border-white/10 hover:bg-white/5"
          >
            <DiscordIcon className="h-4 w-4 mr-2" />
            Discord
          </Button>
        </form>
      </div>
    </div>
  );
}

function SubmitButton({ children }: { children: React.ReactNode }) {
  const { pending } = useFormStatus();

  return (
    <Button
      type="submit"
      className="w-full bg-primary hover:bg-primary/90"
      disabled={pending}
    >
      {pending ? "Creating account..." : children}
    </Button>
  );
}

